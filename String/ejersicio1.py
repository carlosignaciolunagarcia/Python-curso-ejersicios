#variable con mensaje
mensaje= "Te quiero solo como amigo"

print("los dos primeros caracteres:",mensaje[:3])
print("los tres ultimos caracteres:",mensaje[-3:])
print("Cada dos caracteres caracteres:",mensaje[:: 2])
print("Sentido inverso:",mensaje[:: -1])
print("cadena al derecho y al reves:",mensaje+mensaje[:: -1])
